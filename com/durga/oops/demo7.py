class Student:
    def __init__(delf, name, rollno, marks):
        delf.name = name
        delf.rollno = rollno
        delf.marks = marks

    def talk(kelf):
        print('Hello I am: ', kelf.name)
        print('My roll no is: ', kelf.rollno)
        print('My marks are: ', kelf.marks)


s = Student("Sunny", 101, 90)
s.talk()
